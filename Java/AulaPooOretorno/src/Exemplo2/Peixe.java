package Exemplo2;

public class Peixe {
    public int id;
    public String especie;
    public double peso;
    public double tamanho;

    public Peixe(){

    }

    public Peixe(int id, String especie, double peso, double tamanho) {
        this.id = id;
        this.especie = especie;
        this.peso = peso;
        this.tamanho = tamanho;
    }
}
